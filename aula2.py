#-------------------------------------------------------
# Operadores Aritméticos e Expressões
#-------------------------------------------------------

# Soma
a = 10
b = 5
soma = a + b
print("Soma:", soma)  # Output: 15

# # Subtração
subtracao = a - b
print("Subtração:", subtracao)  # Output: 5

# # Multiplicação
multiplicacao = a * b
print("Multiplicação:", multiplicacao)  # Output: 50

# # Divisão
divisao = a / b
print("Divisão:", divisao)  # Output: 2.0

# # Divisão Inteira
divisao_inteira = a // b
print("Divisão Inteira:", divisao_inteira)  # Output: 2

# # Resto da Divisão (Módulo)
resto = a % b
print("Resto da Divisão:", resto)  # Output: 0

# # Potenciação
potencia = a ** b
print("Potenciação:", potencia)  # Output: 100000

# # Exemplo com expressões aritméticas
expressao = (a + b) * (a - b)
print("Expressão:", expressao)  # Output: 75